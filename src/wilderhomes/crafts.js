export const craftCategories = ['All', 'Nature Art', 'Nature Crafts', 'Outdoor Fun', 'Seasonal']

export const getCraftsByCategory = (category) => {
  if (category === 'All') return crafts
  return crafts.filter(craft => {
    const lowerCat = category.toLowerCase()
    if (lowerCat === 'nature art') return craft.id.includes('sun') || craft.id.includes('paint')
    if (lowerCat === 'nature crafts') return craft.id.includes('leaf') || craft.id.includes('grass') || craft.id.includes('dandelion')
    if (lowerCat === 'seasonal') return craft.id.includes('season')
    return true
  })
}
